    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        // validar
        if(txtNombre.getText().trim().length() < 1)
        {
            JOptionPane.showMessageDialog(this, "Falta nombre del cargo");
            txtNombre.requestFocus();
            return;
        }
        
        Cargo cargo = new Cargo();
        cargo.setNombre(txtNombre.getText().trim().toUpperCase());
        
        CCargo controlador = new CCargo();
        boolean res = controlador.agregar(cargo);
        
        if(res)
        {
            JOptionPane.showMessageDialog(this, "Los datos fueron guardados");
            btnLimpiar.doClick();
        }
        else
            JOptionPane.showMessageDialog(this, "Error en la ejecuciÃ³n solicitada");
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel)grilla.getModel();
        modelo.setRowCount(0);      
        
        ArrayList<Cargo> listado = new CCargo().buscarTodos();
        
        for (Cargo cargo : listado) {
            modelo.addRow(new Object[] {
                cargo.getId(),
                cargo.getNombre()
            });
        }
    }//GEN-LAST:event_btnListarActionPerformed

